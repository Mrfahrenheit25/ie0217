# Lista original
my_list = ['p', 'r', 'o', 'g', 'r', 'a', 'm', 'i', 'z']

# Obtener elementos desde el índice 2 hasta el índice 4 (no inclusivo)
print("Sliced items from index 2 to 4:", my_list[2:5])

# Obtener elementos desde el índice 5 hasta el final
print("Sliced items from index 5 to end:", my_list[5:])

# Obtener todos los elementos de la lista (desde el principio hasta el final)
print("Sliced items from beginning to end:", my_list[:])
